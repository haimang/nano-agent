#!/usr/bin/env node
/**
 * RHX2 P1-09 — build-time package-manifest generation.
 *
 * Primary input is `.nano-agent/package-manifest.json` written by
 * `scripts/verify-published-packages.mjs` (P1-08). For local build/test
 * paths where that file may not exist yet, this script synthesizes a
 * workspace-local fallback manifest so the worker bundle still carries a
 * deterministic shape.
 *
 * Supported outputs:
 *
 *   --json                     Print one JSON-encoded manifest
 *   --env-file <path>          Write shell-safe env assignment
 *   --as-typescript-decl <p>   Emit a `.d.ts` declaration
 *   --ts-module <path>         Emit a TS module exporting `NANO_PACKAGE_MANIFEST`
 *
 * Optional:
 *
 *   --worker <name>            Add the current worker name to the manifest
 */

import {
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { createHash } from "node:crypto";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const SELF_DIR = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(SELF_DIR, "..");
const MANIFEST_PATH = resolve(REPO_ROOT, ".nano-agent", "package-manifest.json");
const PUBLISHED_PACKAGES = ["nacp-core", "nacp-session", "jwt-shared"];

function readWorkspaceVersion(name) {
  const path = resolve(REPO_ROOT, "packages", name, "package.json");
  const json = JSON.parse(readFileSync(path, "utf8"));
  if (!json.version || typeof json.version !== "string") {
    throw new Error(`${name}: package.json has no string .version`);
  }
  return json.version;
}

function hashDistDir(name) {
  const dir = resolve(REPO_ROOT, "packages", name, "dist");
  if (!existsSync(dir)) return null;

  const hash = createHash("sha256");
  const files = [];
  function walk(d) {
    for (const ent of readdirSync(d).sort()) {
      const full = resolve(d, ent);
      const st = statSync(full);
      if (st.isDirectory()) walk(full);
      else files.push(full);
    }
  }
  walk(dir);
  for (const f of files.sort()) {
    hash.update(f.slice(dir.length));
    hash.update(readFileSync(f));
  }
  return hash.digest("hex");
}

function buildFallbackManifest() {
  return {
    build_at: new Date().toISOString(),
    packages: PUBLISHED_PACKAGES.map((name) => {
      const version = readWorkspaceVersion(name);
      return {
        name: `@haimang/${name}`,
        workspace_version: version,
        registry_version: version,
        registry_latest_version: version,
        registry_published_at: "workspace-local-fallback",
        dist_sha256: hashDistDir(name),
        match: true,
        resolved_from: "workspace-local-fallback",
      };
    }),
  };
}

function readManifest() {
  if (!existsSync(MANIFEST_PATH)) {
    process.stderr.write(
      `[generate-package-manifest] missing ${MANIFEST_PATH}; using workspace-local fallback for build/test.\n`,
    );
    return buildFallbackManifest();
  }
  return JSON.parse(readFileSync(MANIFEST_PATH, "utf8"));
}

function withWorker(manifest, worker) {
  if (!worker) return manifest;
  return {
    ...manifest,
    worker,
  };
}

function emitJson(manifest) {
  process.stdout.write(JSON.stringify(manifest));
}

function emitEnvFile(manifest, path) {
  const json = JSON.stringify(manifest);
  const escaped = json.replace(/'/g, `'\\''`);
  writeFileSync(path, `WRANGLER_DEFINE_NANO_PACKAGE_MANIFEST='${escaped}'\n`, "utf8");
  process.stderr.write(`[generate-package-manifest] wrote ${path}\n`);
}

function emitDts(manifest, path) {
  const text = [
    `// Auto-generated by scripts/generate-package-manifest.mjs.`,
    `// DO NOT EDIT BY HAND.`,
    ``,
    `declare const __NANO_PACKAGE_MANIFEST__: ${JSON.stringify(manifest, null, 2)};`,
    `export type NanoPackageManifest = typeof __NANO_PACKAGE_MANIFEST__;`,
    ``,
  ].join("\n");
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, text, "utf8");
  process.stderr.write(`[generate-package-manifest] wrote ${path}\n`);
}

function emitTsModule(manifest, path) {
  const text = [
    `// Auto-generated by scripts/generate-package-manifest.mjs.`,
    `// DO NOT EDIT BY HAND.`,
    ``,
    `export const NANO_PACKAGE_MANIFEST = ${JSON.stringify(manifest, null, 2)} as const;`,
    ``,
    `export type NanoPackageManifest = typeof NANO_PACKAGE_MANIFEST;`,
    ``,
    `export function readBuiltPackageManifest(): NanoPackageManifest {`,
    `  return NANO_PACKAGE_MANIFEST;`,
    `}`,
    ``,
  ].join("\n");
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, text, "utf8");
  process.stderr.write(`[generate-package-manifest] wrote ${path}\n`);
}

function main() {
  const argv = process.argv.slice(2);
  let mode = "json";
  let target = null;
  let worker = null;

  let i = 0;
  while (i < argv.length) {
    const a = argv[i++];
    if (a === "--json") mode = "json";
    else if (a === "--env-file") {
      mode = "env-file";
      target = argv[i++];
    } else if (a === "--as-typescript-decl") {
      mode = "dts";
      target = argv[i++];
    } else if (a === "--ts-module") {
      mode = "ts-module";
      target = argv[i++];
    } else if (a === "--worker") {
      worker = argv[i++];
    } else {
      process.stderr.write(`[generate-package-manifest] unknown arg: ${a}\n`);
      process.exit(2);
    }
  }

  const manifest = withWorker(readManifest(), worker);

  if (mode === "json") emitJson(manifest);
  else if (mode === "env-file") emitEnvFile(manifest, target);
  else if (mode === "dts") emitDts(manifest, target);
  else if (mode === "ts-module") emitTsModule(manifest, target);
}

main();
