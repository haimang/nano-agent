/**
 * Generate a human-readable markdown doc from the DurablePromotionRegistry.
 *
 * Output: dist/trace-contract.md
 *
 * The doc is the owner-auditable artefact that answers:
 *   "What do we persist, why, at what granularity, and what makes us
 *    revisit the decision?"
 */

import { writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

import { createDefaultRegistry } from "../src/durable-promotion-registry.js";
import {
  LIVE_ONLY_EVENTS,
  DURABLE_AUDIT_EVENTS,
  DURABLE_TRANSCRIPT_EVENTS,
} from "../src/classification.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const outDir = join(__dirname, "..", "dist");
mkdirSync(outDir, { recursive: true });

const registry = createDefaultRegistry();
const entries = [...registry.list()].sort((a, b) => a.eventKind.localeCompare(b.eventKind));

const lines: string[] = [];
lines.push("# Trace Contract — @nano-agent/eval-observability");
lines.push("");
lines.push("_This file is auto-generated by `scripts/gen-trace-doc.ts`._");
lines.push("");
lines.push("## 1. Three-way classification");
lines.push("");
lines.push("| Layer | Event kinds |");
lines.push("|-------|-------------|");
lines.push(`| live | ${[...LIVE_ONLY_EVENTS].sort().map((k) => `\`${k}\``).join(", ")} |`);
lines.push(`| durable-audit | ${[...DURABLE_AUDIT_EVENTS].sort().map((k) => `\`${k}\``).join(", ")} |`);
lines.push(
  `| durable-transcript | ${[...DURABLE_TRANSCRIPT_EVENTS].sort().map((k) => `\`${k}\``).join(", ")} |`,
);
lines.push("");
lines.push("## 2. Durable promotion registry");
lines.push("");
lines.push("| Event kind | Layer | Granularity | Replay visible | Revisit condition | Description |");
lines.push("|------------|-------|-------------|----------------|-------------------|-------------|");
for (const e of entries) {
  lines.push(
    `| \`${e.eventKind}\` | ${e.layer} | ${e.granularity} | ${e.replayVisible} | ${e.revisitCondition} | ${e.description} |`,
  );
}
lines.push("");

const outPath = join(outDir, "trace-contract.md");
writeFileSync(outPath, lines.join("\n"));
console.log(`✅ Generated trace contract doc → ${outPath}`);
